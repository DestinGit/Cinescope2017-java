    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int id;
        id = Integer.valueOf(jLabelID.getText().toString());
        Genre d = new Genre(id, jTextFieldCode.getText().toString(),
                jTextFieldLibelle.getText().toString(), jTextFieldGenreGrammatical.getText().toString());

        int r = dao.delete(d);
        if (r > 0) {
            try {
                // Validation de l'exécution
                icnx.commit();
                // Maj de la jTableGenre
                int liRow = jTableGenre.getSelectedRow();
                ldtm.removeRow(liRow);

                jButtonCLSActionPerformed(null);

            } catch (SQLException e) {
                System.out.println("Del + lose : " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

