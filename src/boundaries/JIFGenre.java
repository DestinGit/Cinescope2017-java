    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        int id;
        id = Integer.valueOf(jLabelID.getText().toString());

        Genre d = new Genre(id, jTextFieldCode.getText().toString(),
                jTextFieldLibelle.getText().toString(),
                jTextFieldGenreGrammatical.getText().toString());

        int r = dao.update(d);
        if (r > 0) {
            try {
                // Valider l'exécution
                icnx.commit();
                // Maj du tableau
                int liRow = jTableGenre.getSelectedRow();
                jTableGenre.setValueAt(jTextFieldCode.getText().toString(), liRow, 1);
                jTableGenre.setValueAt(jTextFieldLibelle.getText().toString(), liRow, 2);
                jTableGenre.setValueAt(jTextFieldGenreGrammatical.getText().toString(), liRow, 3);

                jButtonCLSActionPerformed(null);

            } catch (SQLException e) {
                System.out.println("Ins + lose : " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        Genre d = new Genre(jTextFieldCode.getText().toString(),
                jTextFieldLibelle.getText().toString(), jTextFieldGenreGrammatical.getText().toString());

        int r = dao.insert(d);
        if (r > 0) {
            try {
                // Valider l'exécution
                icnx.commit();
                // Maj du tableau (jDableGenre)
                Object[] tLigne = new Object[4];
                tLigne[0] = r;
                tLigne[1] = jTextFieldCode.getText().toString();
                tLigne[2] = jTextFieldLibelle.getText().toString();
                tLigne[3] = jTextFieldGenreGrammatical.getText().toString();
                
                ldtm.addRow(tLigne);

                jButtonCLSActionPerformed(null);

            } catch (SQLException e) {
                System.out.println("Ins + lose : " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonInsertActionPerformed


